#include <bits/stdc++.h>
#define ll long long
using namespace std;
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int T;
    cin >> T;
    while (T--)
    {
        int n, l, x;
        cin >> n >> l >> x;
        int a = min(l, (n - l));
        int sum = x * a;
        cout << sum << "\n";
    }
    return 0;
}